// The 'ssl3_mac_*' routines are used with the following mechanisms
//
//    CKM_SSL3_MD5_MAC
//    CKM_SSL3_SHA1_MAC
//

//
//
CK_RV
ssl3_mac_sign( SESSION              * sess,
               CK_BBOOL               length_only,
               SIGN_VERIFY_CONTEXT  * ctx,
               CK_BYTE              * in_data,
               CK_ULONG               in_data_len,
               CK_BYTE              * out_data,
               CK_ULONG             * out_data_len )
{
   OBJECT          * key_obj  = NULL;
   CK_ATTRIBUTE    * attr     = NULL;
   CK_BYTE           hash[SHA1_HASH_SIZE];
   CK_BYTE         * key_data = NULL;
   CK_BYTE           inner[48], outer[48];
   DIGEST_CONTEXT    digest_ctx;
   CK_MECHANISM      digest_mech;
   CK_ULONG          key_bytes, hash_len, mac_len;
   CK_RV             rc;


   if (!sess || !ctx || !out_data_len){
      TRACE_ERROR("%s received bad argument(s)\n", __FUNCTION__);
      return CKR_FUNCTION_FAILED;
   }
   mac_len = *(CK_ULONG *)ctx->mech.pParameter;

   if (length_only == TRUE) {
      *out_data_len = mac_len;
      return CKR_OK;
   }

   if (*out_data_len < mac_len) {
      *out_data_len = mac_len;
      TRACE_ERROR("%s\n", ock_err(ERR_BUFFER_TOO_SMALL));
      return CKR_BUFFER_TOO_SMALL;
   }

   memset( &digest_ctx, 0x0, sizeof(DIGEST_CONTEXT) );

   rc = object_mgr_find_in_map1( ctx->key, &key_obj );
   if (rc != CKR_OK){
      TRACE_ERROR("Failed to acquire key from specified handle");
      if (rc == CKR_OBJECT_HANDLE_INVALID)
	 return CKR_KEY_HANDLE_INVALID;
      else
	 return rc;
   }

   rc = template_attribute_find( key_obj->template, CKA_VALUE, &attr );
   if (rc == FALSE){
      TRACE_ERROR("Could not find CKA_VALUE in the template\n");
      return CKR_FUNCTION_FAILED;
   }
   else {
      key_bytes = attr->ulValueLen;
      key_data  = attr->pValue;
   }

   // unlike an HMAC operation, we don't XOR the key with the 0x36 or 0x5C.
   // we just append 48 bytes to the key data
   //
   memset( inner, 0x36, 48 );
   memset( outer, 0x5C, 48 );

   if (ctx->mech.mechanism == CKM_SSL3_MD5_MAC)
      digest_mech.mechanism   = CKM_MD5;
   else
      digest_mech.mechanism   = CKM_SHA_1;
   digest_mech.ulParameterLen = 0;
   digest_mech.pParameter     = NULL;


   // inner hash
   //
   rc = digest_mgr_init( sess, &digest_ctx, &digest_mech );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Init failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess, &digest_ctx, key_data, key_bytes );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest update failed.\n");
      return rc;
   }
   if (ctx->mech.mechanism == CKM_SSL3_MD5_MAC){
      rc = digest_mgr_digest_update( sess, &digest_ctx, inner, 48 );
   }
   else {
      rc = digest_mgr_digest_update( sess, &digest_ctx, inner, 40 );
   }
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest update failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess, &digest_ctx, in_data, in_data_len );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest update failed.\n");
      return rc;
   }
   hash_len = sizeof(hash);
   rc = digest_mgr_digest_final( sess, FALSE, &digest_ctx,  hash, &hash_len );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest final failed.\n");
      return rc;
   }
   memset( &digest_ctx, 0x0, sizeof(DIGEST_CONTEXT) );


   // outer hash
   //
   rc = digest_mgr_init( sess, &digest_ctx, &digest_mech );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Init failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess, &digest_ctx, key_data, key_bytes );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest update failed.\n");
      return rc;
   }
   if (ctx->mech.mechanism == CKM_SSL3_MD5_MAC)
      rc = digest_mgr_digest_update( sess, &digest_ctx, outer, 48 );
   else
      rc = digest_mgr_digest_update( sess, &digest_ctx, outer, 40 );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest update failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess, &digest_ctx, hash, hash_len );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest update failed.\n");
      return rc;
   }
   hash_len = sizeof(hash);
   rc = digest_mgr_digest_final( sess, FALSE, &digest_ctx, hash, &hash_len );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest final failed.\n");
      return rc;
   }
   memcpy( out_data, hash, mac_len );
   *out_data_len = mac_len;

   return rc;
}


