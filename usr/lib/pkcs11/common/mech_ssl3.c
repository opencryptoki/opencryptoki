static CK_RV
ssl3_sha_then_md5( SESSION   * sess,
                   CK_BYTE   * secret,
                   CK_BYTE   * firstRandom,
                   CK_ULONG    firstRandomLen,
                   CK_BYTE   * secondRandom,
                   CK_ULONG    secondRandomLen,
                   CK_BYTE   * variableData,
                   CK_ULONG    variableDataLen,
                   CK_BYTE   * outBuff )
{
   DIGEST_CONTEXT digest_ctx;
   CK_MECHANISM   digest_mech;
   CK_BYTE        hash[SHA1_HASH_SIZE];
   CK_ULONG       len;
   CK_RV          rc;

   // SHA(variableData + secret + firstRandom + secondRandom)
   //
   memset( &digest_ctx, 0x0, sizeof(DIGEST_CONTEXT) );
   digest_mech.mechanism      = CKM_SHA_1;
   digest_mech.ulParameterLen = 0;
   digest_mech.pParameter     = NULL;

   rc = digest_mgr_init( sess, &digest_ctx, &digest_mech );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Init failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess,
                                  &digest_ctx,
                                  variableData,
                                  variableDataLen );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Update failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess, &digest_ctx, secret, 48 );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Update failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess,
                                  &digest_ctx,
                                  firstRandom,
                                  firstRandomLen );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Update failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess,
                                  &digest_ctx,
                                  secondRandom,
                                  secondRandomLen );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Update failed.\n");
      return rc;
   }
   len = sizeof(hash);
   rc = digest_mgr_digest_final( sess, FALSE, &digest_ctx, hash, &len );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Final failed.\n");
      return rc;
   }

   // MD5(secret + SHA(...))
   //
   memset( &digest_ctx, 0x0, sizeof(DIGEST_CONTEXT) );
   digest_mech.mechanism      = CKM_MD5;
   digest_mech.ulParameterLen = 0;
   digest_mech.pParameter     = NULL;

   rc = digest_mgr_init( sess, &digest_ctx, &digest_mech );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Init failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess, &digest_ctx, secret, 48 );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Update failed.\n");
      return rc;
   }
   rc = digest_mgr_digest_update( sess, &digest_ctx, hash, len );
   if (rc != CKR_OK){
      TRACE_DEVEL("Digest Update failed.\n");
      return rc;
   }
   len = sizeof(hash);
   rc = digest_mgr_digest_final( sess, FALSE, &digest_ctx, hash, &len );

   if (rc == CKR_OK){
      memcpy( outBuff, hash, len );
   }
   else
      TRACE_DEVEL("Digest Final failed.\n");

   return rc;
}

