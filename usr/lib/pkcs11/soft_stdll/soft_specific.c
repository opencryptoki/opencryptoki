CK_RV token_specific_sha(DIGEST_CONTEXT *ctx, CK_BYTE *in_data,
			 CK_ULONG in_data_len, CK_BYTE *out_data,
			 CK_ULONG *out_data_len)
{
	int rc;
	CK_RV rv = CKR_OK;
	EVP_MD_CTX *mdctx;
	unsigned int hlen;
	
	mdctx = (EVP_MD_CTX *)ctx->context;

	if (!ctx)
		return CKR_OPERATION_NOT_INITIALIZED;
	
	switch(ctx->mech.mechanism) {
	case CKM_SHA_1:
		hlen = SHA1_HASH_SIZE;
		break;
	case CKM_SHA256:
		hlen = SHA2_HASH_SIZE;
		break;
	case CKM_SHA384:
		hlen = SHA3_HASH_SIZE;
		break;
	case CKM_SHA512:
		hlen = SHA5_HASH_SIZE;
		break;
	default:
		return CKR_MECHANISM_INVALID;
	}

	if (*out_data_len < hlen) 
		return CKR_BUFFER_TOO_SMALL;

	if (!in_data || !out_data) {
		rv = CKR_ARGUMENTS_BAD;
		goto done;
	}
	
	rc =  EVP_DigestUpdate(mdctx, in_data, in_data_len);
	if (!rc) {
		rv = CKR_FUNCTION_FAILED;
		goto done;
	}

	rc = EVP_DigestFinal(mdctx, out_data, &hlen);	
	if (!rc) 
		rv = CKR_FUNCTION_FAILED;
	else
		*out_data_len = hlen;
		
done:
	EVP_MD_CTX_destroy(mdctx);
	ctx->context = NULL;
	return rv;
}	

CK_RV token_specific_sha_update(DIGEST_CONTEXT *ctx, CK_BYTE *in_data,
				CK_ULONG in_data_len)
{
	int rc;
	EVP_MD_CTX *mdctx;
	CK_RV rv = CKR_OK;
	
	mdctx = (EVP_MD_CTX *)ctx->context;

	if (!ctx)
		return CKR_OPERATION_NOT_INITIALIZED;
	
	if (!in_data) {
		rv = CKR_ARGUMENTS_BAD;
		goto error;
	}

	rc = EVP_DigestUpdate(mdctx, in_data, in_data_len);
	if (!rc)
		rv = CKR_FUNCTION_FAILED;
	else {
		ctx->context = (CK_BYTE *)mdctx;
		return CKR_OK;
	}
	
error:
	EVP_MD_CTX_destroy(mdctx);
	ctx->context = NULL;
	return rv;
}

CK_RV token_specific_sha_final(DIGEST_CONTEXT *ctx, CK_BYTE *out_data,
			       CK_ULONG *out_data_len)
{
	int rc;
	EVP_MD_CTX *mdctx;
	CK_RV rv = CKR_OK;
	unsigned int hlen;

	mdctx = (EVP_MD_CTX *)ctx->context;

	if (!ctx)
		return CKR_OPERATION_NOT_INITIALIZED;
		
	if (!out_data) {
		rv = CKR_ARGUMENTS_BAD;
		goto done;		
	}

	switch(ctx->mech.mechanism) {
	case CKM_SHA_1:
		hlen = SHA1_HASH_SIZE;
		break;
	case CKM_SHA256:
		hlen = SHA2_HASH_SIZE;
		break;
	case CKM_SHA384:
		hlen = SHA3_HASH_SIZE;
		break;
	case CKM_SHA512:
		hlen = SHA5_HASH_SIZE;
		break;
	default:
		return CKR_MECHANISM_INVALID;
	}

	if (*out_data_len < hlen)
		return CKR_BUFFER_TOO_SMALL;

	rc = EVP_DigestFinal(mdctx, out_data, &hlen);	
	if (!rc)
		rv = CKR_FUNCTION_FAILED;
	else
		*out_data_len = hlen;
	
done:
	EVP_MD_CTX_destroy(mdctx);
	ctx->context = NULL;
	return rv;
}
